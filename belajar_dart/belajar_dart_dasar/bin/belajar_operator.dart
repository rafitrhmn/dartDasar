///belajar operator dart.
void main() {
  var nilai_1 = 4;
  var nilai_2 = 90;

  var penjumlahan = nilai_1 + nilai_2;
  var pengurangan = nilai_1 - nilai_2;
  var perkalian = nilai_1 * nilai_2;
  var angka_min = -nilai_1;
  var pembagian_double = nilai_1 / nilai_2;
  var pembagian_int = nilai_1 ~/ nilai_2;
  var sisa_bagi = nilai_1 % nilai_2;

  print('penjumlahan = $penjumlahan');
  print('pengurangan = $pengurangan');
  print('perkalian = $perkalian');
  print('angka min = $angka_min');
  print('pembagian_double = $pembagian_double');
  print('penjumlahan_int = $pembagian_int');
  print('sisa bagi = $sisa_bagi');
}
