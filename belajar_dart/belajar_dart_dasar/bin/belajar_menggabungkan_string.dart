/// belajar mengabungkan string dengan menambahkan "+" diantara variabel
///atau juga bisa string sendiri-sendiri di satu variabel
void main() {
  var nama_awal = 'Rafi';
  var nama_akhir = 'Taufiqurahman';

  var nama_pertama = nama_awal + nama_akhir;
  var nama_kedua = nama_awal + ' ' + nama_akhir;
  var nama_ketiga =
      'ikan' 'hasil' 'mancing'; //string sendiri-sendiri di satu variabel
  var nama_keempat = 'ikan ' 'hasil' ' mancing';

  print(nama_pertama);
  print(nama_kedua);
  print(nama_ketiga);
  print(nama_keempat);
}
